#!/usr/bin/env node
/**
 * build-questions.mjs
 * -------------------
 * Parses the source Markdown quiz files (which CONTAIN the correct answers)
 * and produces two artifacts:
 *
 *   1. docs/data/questions.public.json   -> SAFE to commit + publish.
 *                                            Questions + options only. No answers, no rationale.
 *   2. build-output/answerKey.gs         -> SECRET. Paste into your Apps Script project.
 *                                            Correct option + rationale per question.
 *
 * Run:  npm run build
 *
 * Question format expected (see questions-src/*.md):
 *   ### Q1. <question text>
 *   *[<Topic> · <Scope>]*
 *   - A) <option>
 *   - B) <option>
 *   - C) <option>
 *   - D) <option>
 *   **Answer: D**
 *   *Why:* <rationale>
 *   ---
 */

import { readFileSync, writeFileSync, mkdirSync } from "node:fs";
import { join, dirname } from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const ROOT = join(__dirname, "..");
const SRC_DIR = join(ROOT, "questions-src");
const PUBLIC_OUT = join(ROOT, "docs", "data", "questions.public.json");
const KEY_OUT = join(ROOT, "build-output", "answerKey.gs");

const manifest = JSON.parse(readFileSync(join(ROOT, "manifest.json"), "utf8"));

/** Parse one .md file into an array of question objects. */
function parseQuestions(md, assessmentId) {
  const lines = md.split(/\r?\n/);
  const questions = [];
  let cur = null;

  const push = () => {
    if (!cur) return;
    if (!cur.correct) throw new Error(`[${assessmentId}] ${cur.id} is missing an **Answer:** line`);
    if (cur.options.length < 2) throw new Error(`[${assessmentId}] ${cur.id} has fewer than 2 options`);
    questions.push(cur);
  };

  for (const raw of lines) {
    const line = raw.trim();

    const qMatch = line.match(/^###\s+Q(\d+)\.\s+(.*)$/);
    if (qMatch) {
      push();
      cur = {
        id: `q${qMatch[1]}`,
        text: qMatch[2].trim(),
        topic: "",
        scope: "",
        options: [],
        correct: "",
        why: "",
      };
      continue;
    }
    if (!cur) continue; // ignore file header / coverage summary

    const tagMatch = line.match(/^\*\[(.+?)\]\*$/);
    if (tagMatch) {
      const parts = tagMatch[1].split("·").map((s) => s.trim());
      cur.topic = parts[0] || "";
      cur.scope = parts[1] || "";
      continue;
    }

    const optMatch = line.match(/^-\s+([A-Z])\)\s+(.*)$/);
    if (optMatch) {
      cur.options.push({ id: optMatch[1], text: optMatch[2].trim() });
      continue;
    }

    const ansMatch = line.match(/^\*\*Answer:\s*([A-Z])\*\*/);
    if (ansMatch) {
      cur.correct = ansMatch[1];
      continue;
    }

    const whyMatch = line.match(/^\*Why:\*\s*(.*)$/);
    if (whyMatch) {
      cur.why = whyMatch[1].trim();
      continue;
    }
  }
  push();
  return questions;
}

const publicDoc = {
  generatedAt: new Date().toISOString(),
  passThreshold: manifest.passThreshold,
  maxAttempts: manifest.maxAttempts,
  productLines: [],
};

// answerKey shape: { [assessmentId]: { [questionId]: { correct, why } } }
const answerKey = {};

let totalQuestions = 0;

for (const pl of manifest.productLines) {
  const plOut = { id: pl.id, name: pl.name, assessments: [] };

  for (const a of pl.assessments) {
    const md = readFileSync(join(SRC_DIR, a.file), "utf8");
    const parsed = parseQuestions(md, a.id);

    // sanity check: every question has a correct option that exists
    for (const q of parsed) {
      if (!q.options.some((o) => o.id === q.correct)) {
        throw new Error(`[${a.id}] ${q.id}: answer "${q.correct}" not among options`);
      }
    }

    plOut.assessments.push({
      id: a.id,
      title: a.title,
      level: a.level,
      productLineId: pl.id,
      productLineName: pl.name,
      questionCount: parsed.length,
      // PUBLIC: strip correct + why
      questions: parsed.map((q) => ({
        id: q.id,
        text: q.text,
        topic: q.topic,
        scope: q.scope,
        options: q.options,
      })),
    });

    answerKey[a.id] = Object.fromEntries(
      parsed.map((q) => [q.id, { correct: q.correct, why: q.why }])
    );

    totalQuestions += parsed.length;
    console.log(`  ✓ ${a.id.padEnd(10)} ${String(parsed.length).padStart(3)} questions  (${pl.name})`);
  }
  publicDoc.productLines.push(plOut);
}

mkdirSync(dirname(PUBLIC_OUT), { recursive: true });
mkdirSync(dirname(KEY_OUT), { recursive: true });

writeFileSync(PUBLIC_OUT, JSON.stringify(publicDoc, null, 2));

const keyFile = `/**
 * answerKey.gs — AUTO-GENERATED by scripts/build-questions.mjs
 * ⚠️  SECRET. Paste this whole file into your Apps Script project (File > New > Script "answerKey").
 *     Do NOT commit it to the public repo. Re-generate with \`npm run build\` whenever questions change.
 * Generated: ${publicDoc.generatedAt}
 */
const ANSWER_KEY = ${JSON.stringify(answerKey, null, 2)};
`;
writeFileSync(KEY_OUT, keyFile);

console.log(`\nDone. ${totalQuestions} questions across ${publicDoc.productLines.length} product line(s).`);
console.log(`  public  -> ${PUBLIC_OUT}`);
console.log(`  secret  -> ${KEY_OUT}  (paste into Apps Script, keep private)`);
